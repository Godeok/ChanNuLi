package kr.co.healthcare.diseaseInfo;

public class Diseases
{
    private String id;
    private String name;

    public void setId(String _id) {
        id = _id;
    }

    public void setName(String _name) {
        name = _name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
